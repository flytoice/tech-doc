package com.tianrun.artM.pojo;

/**
 * 微信二级菜单
 * @author flytoice
 *
 */
public class L2Class {
	private int id;	
	private String name;
	private String uuid;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
}
